class SoccerMatch {
  final String team1;
  final String team2;
  final String runningTime;
  final String totalTime;
  final int team1Goal;
  final int team2Goal;
  final String id;

  SoccerMatch({
    required this.team1,
    required this.team2,
    required this.runningTime,
    required this.totalTime,
    required this.team1Goal,
    required this.team2Goal,
    required this.id,
  });
}
